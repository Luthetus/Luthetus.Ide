namespace Luthetus.Common.RazorLib.Dropdowns.Models;

/// <summary>
/// Right: The dropdown will be rendered such that the left edge of the dropdown is aligned
/// on the right edge of the anchor html element.<br/><br/>
///
/// Bottom: The dropdown will be rendered such that the top edge of the dropdown is aligned
/// on the bottom edge of the anchor html element.
/// </summary>
public enum DropdownOrientation
{
	Bottom,
	Right,
}
